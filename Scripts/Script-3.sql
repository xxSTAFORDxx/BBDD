drop table if exists Grups cascade;

CREATE TABLE Grups (
GrupId  serial,
GrupCurs int NOT NULL,
cicle  VARCHAR(5),
GrupNom  VARCHAR(15), 
CONSTRAINT PK_Grup PRIMARY KEY (GrupId));

DROP TABLE if exists Alumnes cascade; 

CREATE TABLE Alumnes (
 AlumneId serial,
 nom  VARCHAR(10) not null,
 cognom    VARCHAR(10) not null,
 dataNaixement date,
 repetidor int default 0,
 actiu int default 1,
 Grup int not null,
 CONSTRAINT PK_Alumnes PRIMARY KEY (AlumneId),
 CONSTRAINT FK_Grups FOREIGN KEY (Grup) REFERENCES Grups (GrupId)
) ;

